@st.cache_data
def load_assets():
    """Load pre-computed data and models."""
    # Load processed data (generated by the notebook)
    # If running standalone, generate synthetic demo data
    try:
        df = pd.read_csv("data/processed_daily.csv", parse_dates=['date'])
        forecast_df = pd.read_csv("models/forecast_30days.csv", parse_dates=['date'])
    except FileNotFoundError:
        # Generate demo data
        np.random.seed(42)
        dates = pd.date_range('2013-01-01', '2017-12-31', freq='D')
        n = len(dates)
        trend = np.linspace(50000, 80000, n)
        seasonal = 10000 * np.sin(2 * np.pi * np.arange(n) / 365.25)
        noise = np.random.normal(0, 3000, n)
        sales = trend + seasonal + noise
        sales = np.clip(sales, 0, None)

        df = pd.DataFrame({
            'date': dates,
            'total_sales': sales,
            'total_onpromotion': np.random.randint(0, 500, n),
            'avg_oil_price': 50 + np.random.normal(0, 5, n).cumsum() * 0.01,
            'is_holiday': np.random.choice([0, 1], n, p=[0.9, 0.1]),
            'regime': np.random.choice([0, 1, 2], n, p=[0.25, 0.5, 0.25]),
            'total_transactions': np.random.randint(10000, 50000, n)
        })
        df['regime_label'] = df['regime'].map(REGIME_NAMES)

        future_dates = pd.date_range('2018-01-01', periods=30, freq='D')
        forecast_df = pd.DataFrame({
            'date': future_dates,
            'total_sales': 72000 + np.random.normal(0, 2000, 30),
            'regime': np.random.choice([0,1,2], 30),
            'regime_label': np.random.choice(list(REGIME_NAMES.values()), 30)
        })

    return df, forecast_df

df, forecast_df = load_assets()

